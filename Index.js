
/*The script helps to change the ICON Size when the Mouse in In or Out of the Font-Awesome-ICON*/

/*Script for th Ingredients*/
function ingredientsHover(){
    document.getElementById('ingredients').firstElementChild.firstElementChild.style.fontSize = "300%"; /*When mouse touch the ICON*/
}

function ingredientsNormal(){
    document.getElementById('ingredients').firstElementChild.firstElementChild.style.fontSize = "100%"; /*When mouse IS OUT OF the ICON*/
}


